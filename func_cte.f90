subroutine math_cte(pi)  ! Mathematical constants of frequent use
implicit none
real(8) :: pi

pi = 4.d0*datan(1.d0)

end
!-----------------------------------------------------------------------------------------------------------------------------------
!                                               FUCNTIONS (several)
!-----------------------------------------------------------------------------------------------------------------------------------
real(8) function log2(x)  ! Returns the base two log of x
implicit none
real(8):: x

log2 = dlog(x)/dlog(2.d0)

end
