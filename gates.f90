!-----------------------------------------------------------------------------------------------------------------------------------
!                                              Matrices for quantum gates
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_H(H)
implicit none
complex(8) :: H(1:2,1:2)
real(8) :: z = 1.d0/sqrt(2.d0)

H(1,1) = z ;   H(1,2) = z ;   H(2,1) = z ;   H(2,2) = -z 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_id(id)
implicit none
complex(8) :: id(1:2,1:2)

id(1,1) = 1.d0 ;   id(1,2) = 0.d0 ;   id(2,1) = 0.d0 ;   id(2,2) = 1.d0 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_X(X)
implicit none
complex(8) :: X(1:2,1:2)

X(1,1) = 0.d0 ;   X(1,2) = 1.d0 ;   X(2,1) = 1.d0 ;   X(2,2) = 0.d0 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_Y(Y)
implicit none
complex(8) :: Y(1:2,1:2)

Y(1,1) = 0.d0 ;   Y(1,2) = -(0.d0,1.d0) ;   Y(2,1) = (0.d0,1.d0) ;   Y(2,2) = 0.d0 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_Z(Z)
implicit none
complex(8) :: Z(1:2,1:2)

Z(1,1) = 1.d0 ;   Z(1,2) = 0.d0 ;   Z(2,1) = 0.d0 ;   Z(2,2) = -1.d0 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_S(S)
implicit none
complex(8) :: S(1:2,1:2)

S(1,1) = 1.d0 ;   S(1,2) = 0.d0 ;   S(2,1) = 0.d0 ;   S(2,2) = (0.d0,1.d0) 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_Sd(Sd)
implicit none
complex(8) :: Sd(1:2,1:2)

Sd(1,1) = 1.d0 ;   Sd(1,2) = 0.d0 ;   Sd(2,1) = 0.d0 ;   Sd(2,2) = -(0.d0,1.d0) 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_T(T)
implicit none
complex(8) :: T(1:2,1:2)

T(1,1) = 1.d0 ;   T(1,2) = 0.d0 ;   T(2,1) = 0.d0 ;   T(2,2) = (1.d0,1.d0)/sqrt(2.d0) 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_Td(Td)
implicit none
complex(8) :: Td(1:2,1:2)

Td(1,1) = 1.d0 ;   Td(1,2) = 0.d0 ;   Td(2,1) = 0.d0 ;   Td(2,2) = (1.d0,-1.d0)/sqrt(2.d0) 

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_U1(lambda,U1)
implicit none
complex(8) :: U1(1:2,1:2)
real(8) :: lambda

U1(1,1) = 1.d0 ;   U1(1,2) = 0.d0 ;   U1(2,1) = 0.d0 ;   U1(2,2) = exp((0.d0,1.d0)*lambda)

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_U2(lambda,phi,U2)
implicit none
complex(8) :: U2(1:2,1:2)
real(8) :: lambda, phi

U2(1,1) = 1.d0 ;   U2(1,2) = -exp((0.d0,1.d0)*lambda)
U2(2,1) = exp((0.d0,1.d0)*phi) ;   U2(2,2) = exp((0.d0,1.d0)*(lambda+phi))

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_U3(theta,lambda,phi,U3)
implicit none
complex(8) :: U3(1:2,1:2)
real(8) :: theta,lambda, phi

U3(1,1) = cos(theta/2.d0) ;   U3(1,2) = -sin(theta/2.d0)*exp((0.d0,1.d0)*lambda)
U3(2,1) = sin(theta/2.d0)*exp((0.d0,1.d0)*phi) ;   U3(2,2) = cos(theta/2.d0)*exp((0.d0,1.d0)*(lambda+phi))

end
!-----------------------------------------------------------------------------------------------------------------------------------
subroutine gate_CNOT(CNOT)
implicit none
complex(8) :: CNOT(4,4)

 CNOT = 0.d0
 CNOT(1,1) = 1.d0 ;   CNOT(2,2) = 1.d0 ;   CNOT(3,4) = 1.d0 ;   CNOT(4,3) = 1.d0 

end
